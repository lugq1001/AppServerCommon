package com.appserver.common.network;

public class BaseRequest {
	
	private String s = "";
	
	private String version = "0";

	public String getS() {
		return s;
	}

	public void setS(String s) {
		this.s = s;
	}

	public String getVersion() {
		return version;
	}

	public void setVersion(String version) {
		this.version = version;
	}

}
